// Motion configuration for expressive animations
data class ExpressiveMotion(
    val standardSpring: androidx.compose.animation.core.SpringSpec<Float> = spring(
        dampingRatio = Spring.DampingRatioMediumBouncy,
        stiffness = Spring.StiffnessMedium
    ),
    val emphasisSpring: androidx.compose.animation.core.SpringSpec<Float> = spring(
        dampingRatio = Spring.DampingRatioLowBouncy,
        stiffness = Spring.StiffnessLow
    ),
    val quickSpring: androidx.compose.animation.core.SpringSpec<Float> = spring(
        dampingRatio = Spring.DampingRatioNoBouncy,
        stiffness = Spring.StiffnessHigh
    )
)

val LocalExpressiveMotion = staticCompositionLocalOf { ExpressiveMotion() }

object KanakkuTheme {
    val motion: ExpressiveMotion
        @Composable
        @ReadOnlyComposable
        get() = LocalExpressiveMotion.current
}

@Composable
fun KanakkuTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    CompositionLocalProvider(
        LocalExpressiveMotion provides ExpressiveMotion()
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = ExpressiveTypography,
            shapes = ExpressiveShapes,
            content = content
        )
    }
}
